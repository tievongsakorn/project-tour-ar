        <p>
        อำเภอบ้านตาก สันนิษฐานว่าคือตัวเมืองตากเดิม เป็นเมืองหน้าด่านสำหรับป้องกันข้าศึกศัตรูมาแต่ดึกดำบรรพ์ 
        ตั้งอยู่บนดอยเล็กๆ ลูกหนึ่ง ปัจจุบันคือบ้านท่าพระธาตุ หมู่ที่ 3 ตำบลเกาะตะเภา อยู่ทางทิศตะวันตกของแม่น้ำปิง 
        ต่อมาสมัยสมเด็จพระมหาธรรมราชา แห่งกรุงศรีอยุธยา ได้ย้ายเมืองตากจากที่เดิมมาอยู่ที่ตำบลป่ามะม่วง อำเภอเมืองตาก 
        และในสมัยพระบาทสมเด็จพระพุทธเลิศล้านภาลัย แห่งกรุงรัตนโกสินทร์ ได้ย้ายเมืองตากไปอยู่ที่ตำบลระแหง อำเภอเมืองตาก 
        ฝั่งตะวันออกของแม่น้ำปิง ซึ่งเป็นที่ตั้งตัวจังหวัดตากในปัจจุบันนี้
เมื่อได้ย้ายเมืองตากไปตั้งที่ใหม่แล้ว ไม่อาจทราบได้ว่า สภาพของเมืองตาก เดิมจะมีฐานะเป็นอำเภอ ตำบลหรือหมู่บ้าน 
แต่พอจะมีหลักฐานว่าที่ว่าการอำเภอบ้านตากได้ตั้งขึ้นที่ฝั่งตะวันตกของแม่น้ำปิง ในท้องที่หมู่ที่ 8 ตำบลตากตกในปัจจุบัน เมื่อประมาณปี พ.ศ. 2438 ถึง
2440 โดยมีหลวงพรมสุภา(นายโต กาญจนสูต) เป็นนายอำเภอคนแรก มีพื้นที่ทำการปกครองรวมทั้งท้องที่อำเภอสามเงาในปัจจุบันเข้าไว้ด้วย ต่อมาเมื่อปี พ.ศ. 
2475 ทางราชการได้ยกฐานะตำบลสามเงาขึ้นเป็น กิ่งอำเภอขึ้นกับอำเภอบ้านตาก และทางราชการได้ยกฐานะกิ่งอำเภอสามเงาขึ้นเป็น อำเภอสามเงา ในโอกาสต่อมา
 เมื่อปี พ.ศ. 2501[1]
