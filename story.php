        <p>
        เมืองตากในอดีตเป็นเมืองที่มีชาวมอญอยู่มาก่อน ดังมี หลักฐานศิลปมอญปรากฏอยู่ที่อำเภอบ้านตาก มีประวัติความเป็นมาเก่าแก่ 
        และเป็นหนึ่งในหัวเมืองที่มีอายุขัยเกินกว่าสองพันปีขึ้นไป เมื่อมีการ อพยพ ของชนชาติไทยจากลุ่มน้ำแยงซีเกียงตอนใต้ 
        ลงมาตามแนวลำน้ำดง (ลำน้ำสาละวิน) มีพวกหนึ่งได้ข้ามลำน้ำสาละวิน ผ่านลุ่มน้ำเมยหรือแม่น้ำต่องยินเข้ามาทางช่องเขาด้าน 
        อำเภอแม่สอดและมาถึงบริเวณที่ปัจจุบันเรียกว่า “เมืองตาก” ผู้นำกลุ่มคนไทยที่อพยพมาตั้งถิ่นฐานที่เมืองตากในยุค นั้น 
        ได้ตั้งตนเป็นกษัตริย์ปกครองสืบทอดต่อเนื่อง กันมาจนถึงปี พ.ศ. 560 รัชสมัยพระเจ้าสักดำ ซึ่งเป็นกษัตริย์เมืองตากที่ยิ่งใหญ่มาก 
        มีอาณาเขตที่อยู่ในอำนาจแผ่ไปจนจรดทะเลอันดามันดังมีบันทึกในพงศาวดารเหนือกล่าวว่าในรัชสมัยพระเจ้าสักดำนั้นเมืองตากมีการค้าขาย
        กับเมืองอินเดียด้วยเมืองตากคงจะเสื่อมลงในช่วงพุทธศตวรรษที่ 10 พระยากาฬวรรณดิสผู้เป็นกษัตริย์เมืองตากที่อพยพมาจาก 
        ตอนใต้ของลุ่มแม่น้ำแยงซีเกียง ได้โยกย้ายไปสร้างราชธานีขึ้นใหม่ที่เมืองละโว้ทางตอนใต้ของเมืองตากลงไปอีก
        มีบางยุคเมืองตากถูกทอดทิ้ง กลายเป็นเมืองร้างดังในพงศาวดารเหนือได้ กล่าวถึงการเสด็จทางชลมารคของพระนางจามเทวี 
        พระราชธิดากษัตริย์ละโว้ (พระยากาฬวรรณดิส) เพื่อไปปกครองแคว้นหริภุญไชย (ลำพูน) ในราว พ.ศ. 1176 โดยทาง 
        ลำน้ำปิง พระนางจามเทวีขึ้นไปสำรวจบนฝั่งแม่น้ำพบร่องรอยกำแพงเมืองเก่าๆ ถูกทิ้งร้าง จึงโปรดให้สร้างเป็นบ้านเมืองใหม่ชื่อว่า 
        “เมืองตาก” ต่อมาเมื่อ พ.ศ. 1805 ขุนสามชนเจ้าเมืองฉอดได้ยกทัพ มาประชิดเมืองตาก ซึ่งเป็นเมืองชายแดนของกรุงสุโขทัย 
        พ่อขุนศรีอินทราทิตย์ทรงจัดกองทัพออกไปรบ โดยมีพระราชโอรสองค์เล็กซึ่งมีพระชนมายุได้ 19 พรรษา ติดตามไปด้วย กองทัพ 
        ทั้งสองฝ่ายปะทะกันที่บริเวณเชิงดอยนอกเมืองตากประมาณกิโลเมตรเศษ ราชโอรสองค์เล็กได้ทรงชนช้าง กับขุนสามชนกระทำยุทธหัตถีกัน 
        ขุนสามชนสู้ไม่ได้แตกพ่ายไป ต่อมาภายหลังทรงพระนามว่า “พ่อขุนรามคำแหงมหาราช” และได้โปรดสร้างเจดีย์ขึ้นเป็นที่ระลึกถึงชัยชนะ
        ในการทำยุทธหัตถีครั้งนั้น องค์หนึ่งเป็นศิลปแบบสุโขทัย ซึ่งเจดีย์ยุทธหัตถีนี้อยู่ที่วัดพระบรมธาตุ อำเภอบ้านตาก ห่างจากตัวเมืองไป
        ทางฝั่งตะวันตกของแม่น้ำปิงราว 31 กิโลเมตรต่อมาในแผ่นดินมหาธรรมราชา</p>
